import { datasetVersions } from "../../../../api/dataset.ts";
import { datasets } from "../../../../api/dataset.ts";
import { config } from "../../../../config.ts";
import * as psFlags from "../../../../flags.ts";
import { asserts } from "../../../../lib/asserts.ts";
import { dataTable } from "../../../../lib/data-table.ts";
import { loading } from "../../../../lib/loading.ts";
import { pickJson } from "../../../../lib/pick-json.ts";
import { select } from "../../../../prompts/select.ts";
import { z } from "../../../../zcli.ts";
import { args } from "../../../../zcli.ts";
import { command } from "../../../../zcli.ts";
import { defaultFields } from "../mod.ts";

/**
 * This variable is automatically generated by `zcli add`. Do not remove this
 * or change its name unless you're no longer using `zcli add`.
 */
const subCommands: ReturnType<typeof command>[] = [];

export const list = command("list", {
  short: "List dataset versions.",
  long: ({ root }) => `
    List dataset versions in your team or project.

    Pick a subset of fields to display:
    \`\`\`
    ${root.name} dataset versions list <dataset-id> -F name -F dtModified
    \`\`\`
  `,
  commands: subCommands,
  args: args().tuple([
    z.string().describe("The ID of the dataset to get"),
  ]).optional(),
  flags: psFlags.paginator,
  // We use command metadata in the `persistentPreRun` function to check if a
  // command requires an API key. If it does, we'll check to see if one is
  // set. If not, we'll throw an error.
  meta: {
    requireApiKey: true,
  },
}).run(
  async function* ({ args, flags }) {
    const team = await config.get("team");
    asserts(team, "You must be in a team to list datasets.");

    let [datasetId] = args;

    if (!datasetId) {
      const existingProjects = await loading(datasets.list({ limit: 50 }), {
        enabled: !flags.json,
      });
      asserts(existingProjects.ok, existingProjects);

      const selected = await select(
        "Select a dataset:",
        existingProjects.data.items,
        {
          filter(input, option) {
            return (option.name ?? "").toLowerCase().startsWith(input);
          },
          renderOption(option, isSelected) {
            return `${isSelected ? ">" : " "} ${option.name ?? "(no name)"}`;
          },
        },
      );

      asserts(selected, "No dataset selected.");
      datasetId = selected.id;
    }

    const result = await loading(
      datasetVersions.list({
        datasetId,
        limit: flags.limit,
        after: flags.after,
        order: flags.asc ? "asc" : undefined,
      }),
      { enabled: !flags.json },
    );

    asserts(result.ok, result);

    if (!flags.json) {
      for await (
        const line of dataTable(
          result.data.items,
          flags.fields ?? defaultFields,
        )
      ) {
        yield line;
      }
    } else {
      yield pickJson(result.data, flags.fields);
    }
  },
);
